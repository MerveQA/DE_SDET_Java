package Melisa;

public class For_Loops_HatalıLoop {
    public static void main(String[] args) {
        // Loop döngüsü "for" içindeki conditonals lar uygun olmadıgında çalışmaz.

        for (int i = 0; i > 5 ; i++) {
            System.out.println(i);
        }

        System.out.println("Baslangic degeri bitis kosulunu saglamadigindan\n"+
                " loop body si hic calismadan sona erer");
    }

}
